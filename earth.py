def is_land(lat: float, lng: float) -> bool:
    """
    Check if a coordinate is land using the pre-computed raster mask
    derived from Natural Earth 110m land polygons.

    Falls back to bounding-box heuristic if mask not available.
    """
    if _LAND_MASK is not None:
        r = int((_MASK_LAT_MAX - lat) / _MASK_RESOLUTION)
        c = int((lng - _MASK_LNG_MIN) / _MASK_RESOLUTION)
        r = max(0, min(r, _MASK_ROWS - 1))
        c = max(0, min(c, _MASK_COLS - 1))
        return bool(_LAND_MASK[r, c])

    # Fallback: bounding-box heuristic
    return _is_land_bbox(lat, lng)


def is_land_array(lats: np.ndarray, lngs: np.ndarray) -> np.ndarray:
    """Vectorized land check for arrays of coordinates."""
    if _LAND_MASK is not None:
        rows = np.clip(
            ((_MASK_LAT_MAX - lats) / _MASK_RESOLUTION).astype(int),
            0, _MASK_ROWS - 1
        )
        cols = np.clip(
            ((lngs - _MASK_LNG_MIN) / _MASK_RESOLUTION).astype(int),
            0, _MASK_COLS - 1
        )
        return _LAND_MASK[rows, cols]

    # Fallback
    return np.array([_is_land_bbox(lat, lng) for lat, lng in zip(lats, lngs)])


def _is_land_bbox(lat: float, lng: float) -> bool:
    """Fallback bounding-box land detection (used if mask not available)."""
    for region in CONTINENTAL_REGIONS:
        for box in region["detail"]:
            lat_min, lat_max, lng_min, lng_max = box
            if lat_min <= lat <= lat_max and lng_min <= lng <= lng_max:
                return True
    return False


